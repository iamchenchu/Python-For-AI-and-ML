# 2nd type of functions are built in type 
# which are already system defined to perform some specific tasks 

a = [1,2,4,5,67,87,43]
print(sum(a))               # sum() is an inbuilt function to sum all the numbers in a list or a tuple
print(len(a))               # len() prints the size of the list or tuple
print("Hello World")        # print() is also an inbuilt function which prints the statements which we pass within this 

