"""
Conditional Operators : >, >=, <, <=, ==, !

to ask quetions related to above operators we require the keyword if 
"""

x = int(input("what is x ? "))
y = int(input("what is x ? "))

if x < y :
    print("x is less than y")

if x > y :
    print("x is greater than y")

if x == y :
    print("x is equal to y")
